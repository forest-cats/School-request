
$(function() {
     $('.slick-box').slick({
          infinite: true, //無限スクロール
          slidesToShow: 1, //スライドが見える数
          slidesToScroll: 1, //スライドのスクロール数
          arrows: false,
          fade: true, //切り替え時のフェードイン設定
          asNavFor: '.slick-nav',
     });
     $('.slick-nav').slick({
          infinite: true,
          slidesToShow: 4,
          slidesToScroll: 1,
          asNavFor: '.slick-box',
          focusOnSelect: true,
          autoplay: true, //自動再生
          pauseOnHover: true, //自動再生時スライドエリアにマウスオンで一時停止
     });
});



